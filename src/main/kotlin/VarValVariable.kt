fun main() {
    // var adalah jenis varible yang dapat di ubah nilai datanya disebut sebagai mutable
    var address = "kota"
    address = "Cilegon"
    println("$address")

    // Tipe data String
    val variableA = "Ada"
    val variableB = "Tikus"
    println(variableA + variableB)

    // Tipe data Integer
    val nilaiA = 10
    val nilaiB = 90
    val hasil = nilaiA + nilaiB
    println(hasil)

    //Tipe data Float
    val komaA = 1.5F
    val komaB = 1.3F
    val hasilKoma = komaA + komaB
    println(hasilKoma)

    
}